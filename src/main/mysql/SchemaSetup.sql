    BRANCH 			VARCHAR(255) NOT NULL,
	GIT_PATH 			VARCHAR(255) NOT NULL,
    HASH_CODE 		VARCHAR(255) NOT NULL,
    COMMIT_ID 		VARCHAR(255) NOT NULL,
